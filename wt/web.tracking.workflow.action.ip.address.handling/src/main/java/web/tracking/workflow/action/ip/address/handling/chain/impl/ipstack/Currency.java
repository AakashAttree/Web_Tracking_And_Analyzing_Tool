package web.tracking.workflow.action.ip.address.handling.chain.impl.ipstack;

public class Currency {

	  private String code;
	  private String name;
	  private String plural;
	  private String symbol;
	  private String symbol_native;


	 // Getter Methods 

	  public String getCode() {
	    return code;
	  }

	  public String getName() {
	    return name;
	  }

	  public String getPlural() {
	    return plural;
	  }

	  public String getSymbol() {
	    return symbol;
	  }

	  public String getSymbol_native() {
	    return symbol_native;
	  }

	 // Setter Methods 

	  public void setCode( String code ) {
	    this.code = code;
	  }

	  public void setName( String name ) {
	    this.name = name;
	  }

	  public void setPlural( String plural ) {
	    this.plural = plural;
	  }

	  public void setSymbol( String symbol ) {
	    this.symbol = symbol;
	  }

	  public void setSymbol_native( String symbol_native ) {
	    this.symbol_native = symbol_native;
	  }

}
